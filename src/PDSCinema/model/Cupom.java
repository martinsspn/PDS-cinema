package PDSCinema.model;

public class Cupom {
	String codigo;
	double tipoDeCupom;


	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public double getTipoDeCupom() {
		return tipoDeCupom;
	}

	public void setTipoDeCupom(double tipoDeCupom) {
		this.tipoDeCupom = tipoDeCupom;
	}
}
